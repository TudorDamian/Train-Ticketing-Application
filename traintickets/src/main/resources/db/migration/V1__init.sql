create table stations (
    id bigint generated by default as identity primary key,
    name varchar(255) not null,
    code varchar(16) not null unique
);

create table routes (
    id bigint generated by default as identity primary key,
    name varchar(255) not null
);

create table route_stops (
    id bigint generated by default as identity primary key,
    route_id bigint not null references routes(id) on delete cascade,
    station_id bigint not null references stations(id),
    stop_order integer not null,
    arrival_offset_minutes integer not null,
    departure_offset_minutes integer not null,
    unique(route_id, station_id),
    unique(route_id, stop_order)
);

create table trains (
    id bigint generated by default as identity primary key,
    name varchar(255) not null,
    capacity integer not null check (capacity > 0)
);

create table trips (
    id bigint generated by default as identity primary key,
    train_id bigint not null references trains(id),
    route_id bigint not null references routes(id),
    departure_datetime timestamp not null,
    status varchar(32) not null,
    delay_minutes integer not null default 0,
    version bigint not null default 0
);

create table bookings (
    id bigint generated by default as identity primary key,
    customer_email varchar(255) not null,
    trip_id bigint not null references trips(id),
    from_station_id bigint not null references stations(id),
    to_station_id bigint not null references stations(id),
    ticket_count integer not null check (ticket_count > 0),
    status varchar(32) not null,
    created_at timestamp with time zone not null
);

create table tickets (
    id bigint generated by default as identity primary key,
    booking_id bigint not null references bookings(id) on delete cascade,
    passenger_name varchar(255)
);

create table delay_events (
    id bigint generated by default as identity primary key,
    trip_id bigint not null references trips(id),
    delay_minutes integer not null,
    reason varchar(255) not null,
    created_at timestamp with time zone not null
);

